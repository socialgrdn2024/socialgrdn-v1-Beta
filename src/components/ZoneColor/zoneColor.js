

export default function zoneColor(zone) {

    if (zone === '0a') {
        return '#d7bde2';
    } else if (zone === '0b') {
        return '#c39bd3';
    } else if (zone === '1a') {
        return '#7fb3d5';
    } else if (zone === '1b') {
        return '#a9cce3';
    } else if (zone === '2a') {
        return '#a3e4d7';
    } else if (zone === '2b') {
        return '#7dcea0';
    } else if (zone === '3a') {
        return '#28b463';
    } else if (zone === '3b') {
        return '#a9dfbf';
    } else if (zone === '4a') {
        return '#BCE864';
    } else if (zone === '4b') {
        return '#f4d03f';
    };

}